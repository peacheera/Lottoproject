package exexception;

public class Person {


	String name;
	String job;
	
	public Person() {
		
	}
	
	
	public Person(String name, String job) {
		super();
		this.name = name;
		this.job = job;
	}

	
	public String getName() {
		return name;
	}


	public String getJob() {
		return job;
	}
	
}